package com.gz.medicine.gzyun.health.reponse;

public class IntervalDateReponse {

    private  String schedulingdays;

    private  String formdate;

    private  String Intervalate;


    public IntervalDateReponse() {
    }

    public String getSchedulingdays() {
        return schedulingdays;
    }

    public void setSchedulingdays(String schedulingdays) {
        this.schedulingdays = schedulingdays;
    }

    public String getFormdate() {
        return formdate;
    }

    public void setFormdate(String formdate) {
        this.formdate = formdate;
    }

    public String getIntervalate() {
        return Intervalate;
    }

    public void setIntervalate(String intervalate) {
        Intervalate = intervalate;
    }
}
